- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Cell";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:CellIdentifier];
    }
    
    // Configure the cell...
    ModuleClass *cls = [classes objectAtIndex:indexPath.row];
    cell.textLabel.text = [NSString stringWithFormat:@"%@ %@",cls.type,cls.classNumber];
    
    NSMutableString *ven;
    int idx = 0;
    for(ModuleClassDetail *d in cls.details) {
        if(idx) [ven appendString:@" / "];
        [ven appendString:d.venue];
    }
    cell.detailTextLabel.text = ven;
    
    return cell;
}

